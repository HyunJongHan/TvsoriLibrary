package com.tvsori.library.enums

enum class InCmd {
    DATA, PING, MYINFO, ROOMINFO, ROOMLIST,
    USERINFO, USERLIST, BOARDMSGINFO, BOARDMSGLIST, CREATEROOM,
    DELETEROOM, ENTERROOM, EXITROOM, ADDBOARDMSG, DELETEBOARDMSG,
    VOTEBOARDMSG, ADDCHANNEL, DELETECHANNEL, PLAYSTATUS, CHANGEDJ,
    CHANGEOWNER, CHANGEUSERINFO, CHANGEUSERMODE, CHANGEROOMINFO, CHATMSG,
    ONE2ONECHAT, PRIVMSG, INVITE, COINS, COINSPAY,
    CHECKRIGHTS, BOARDSONGINFO, BOARDSONGLIST, ADDBOARDSONG, DELETEBOARDSONG,
    MOVIEINFO, MOVIELIST, ADDBOARDMOVIE, DELETEBOARDMOVIE, SWAPBOARDMOVIE,
    NOREINFO, ITEMSHOP, LAYOUT, APPOPTIONBITS, ONE2ONETEXTCHAT,
    ROOMEVENT, MAKEFRIEND, ADMINCMD, KICKOUT, ROOMMEMBER,
    BET, BID, WEBBROWSEREH, SOLICIT, APPOPTIONSTRING,
    SETDEVICEPARAM, SWAPBOARDSONG, LOGOUT, SETTOPBOX, NONE;

    companion object {
        private val VALUES = values()

        fun fromOrdinal(ordinal: Int): InCmd {
            return if (ordinal >= 0 && VALUES.size > ordinal) {
                VALUES[ordinal]
            } else NONE
        }
    }
}
